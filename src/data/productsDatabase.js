/**
 * Helper function to get category with all variants
 */
export const getCategoryWithVariants = (categoryId) => {
  const category = productsDatabase.categories.find((c) => c.id === categoryId);
  if (!category) return null;

  const variants = Object.values(productsDatabase.variants).filter(
    (v) => v.categoryId === categoryId
  );

  return { ...category, variants };
};

/**
 * Helper function to get single variant
 */
export const getVariantBySlug = (slug) => {
  return productsDatabase.variants[slug] || null;
};

/**
 * Helper function to get all variants of a category
 */
export const getCategoryVariants = (categoryId) => {
  return Object.values(productsDatabase.variants).filter(
    (v) => v.categoryId === categoryId
  );
};
